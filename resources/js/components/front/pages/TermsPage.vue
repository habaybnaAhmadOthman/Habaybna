<p dir="rtl">لائـــحة المبــــادئ الأخلاقية</p>

<p dir="rtl">إن موقع حبايبنا.نت&nbsp; هو موقع معني بنشر معلومات عن ذوي التحديات الذهنية والتأخر النمائي للمستخدمين عبر صفحات مختلفة من الموقع ( نشرة حبايبنا، خدمة المكالمات واهداء مكالمة، اسئلة وأجوبة ، مقالات حبايبنا ، دردشة حبايبنا، صور ، فيديو ، دليل حبايبنا ، مباشر وغيرها ) . لائحة المبادئ الاخلاقية هي بمثابة اتفاقية بين المستخدمين كافة والفريق المراقب للموقع ، وذلك بان المستخدمين وهم ( المختصين وغير المختصين ،أسر، والمحررين الذين يكتبون للموقع والذين يتصفحون الموقع&nbsp; وبشكل عام كل من له علاقة بالموقع من فريق العمل أو الداعمين او المستخدمين أو المتابعين ) يعطون الحق المطلق لفريق الموقع في تعديل ، ازالة، حذف المحتوى المقدم من المستخدمين في أي من الحالات التالية : -</p>

