<p dir="rtl">الاتفاقية الكاملة</p>

<p dir="rtl">في حال أخفقنا في ممارسة أو إنفاذ أي حق أو شرط من شروط الخدمة هذه فهذا لا يشكل تنازلا عن هذا الحق أو الحكم</p>

<p dir="rtl">شروط الخدمة هذه وأي سياسات أو قواعد تشغيل منشورة من قبلنا على هذا الموقع أو فيما يتعلق بالخدمة تشكل كامل الاتفاق والتفاهم بينك وبيننا وتحكم استخدامك للخدمة، وتحل محل أي اتفاقيات سابقة أو معاصرة، أو أي تواصل بيننا، سواء كان شفوياً أو مكتوباً (بما في ذلك، ولكن لا تقتصر على، أي إصدارات سابقة من شروط الخدمة).</p>

<p dir="rtl">أي غموض في تفسير شروط الخدمة هذه لا يجوز تفسيره ضد الطرف الذي صاغه.</p>

<p dir="rtl">القانون الواجب تطبيقه</p>

<p dir="rtl">تخضع شروط الخدمة هذه وأية اتفاقيات منفصلة حيث نقدم الخدمات لك بموجبها للقوانين السارية في المملكة الاردنية الهاشمية.</p>

<p dir="rtl">التغييرات على شروط الخدمة</p>

<p dir="rtl">يمكنك مراجعة أحدث إصدار من بنود الخدمة في أي وقت على هذه الصفحة.</p>

<p dir="rtl">&nbsp;</p>

<p dir="rtl">نحن نحتفظ بالحق، وفقا لتقديرنا الخاص، لتحديث أو تغيير أو استبدال أي جزء من شروط الخدمة هذه عن طريق نشر التحديثات والتغييرات على موقعنا. ومن مسؤوليتك زيارة موقعنا بشكل دوري لمتابعة التغييرات. إن الاستمرار في استخدام أو الدخول إلى موقعنا الإلكتروني أو الخدمة بعد نشر أي تغييرات على بنود الخدمة يشكل قبولا لهذه التغييرات.</p>

