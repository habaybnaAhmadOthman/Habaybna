<p dir="rtl">بالإضافة إلى الاستخدامات المحظورة الأخرى المنصوص عليها في شروط الخدمة، يحظر عليك استخدام الموقع أو محتواه: (أ) لأي غرض غير مشروع (ب) لاستدراج الآخرين للقيام بأية أعمال غير مشروعة أو المشاركة فيها (ج) انتهاك أي أنظمة أو قواعد أو قوانين أو تعليمات محلية أو أو إقليمية أو حكومية (د) انتهاك حقوق الملكية الفكرية الخاصة بنا أو حقوق الملكية الفكرية للآخرين (هـ) التحرش أو الإيذاء أو الإهانة أو الشتم أو التشهير أو الذم أو الخداع أو التخويف أو التمييز على أساس الجنس أو الدين أو العرق أو السن أو الأصل أو الإعاقة (و) تقديم معلومات خاطئة أو مضللة (ز) تحميل أو نقل الفيروسات أو أي نوع آخر من الشيفرات الخبيثة التي تستخدم أو يمكن استخدامها بأي شكل من الأشكال للتأثير على وظيفة أو تشغيل الخدمة أو أي موقع على شبكة الإنترنت ذات الصلة&nbsp; أو على مواقع أخرى أو الإنترنت. (ح) جمع أو تتبع المعلومات الشخصية للآخرين (ط) إرسال رسائل البريد الغير مرغوب فيه مثل&nbsp;</p>

<p dir="rtl">(phish, pharm, pretext, spider, crawl or scrape). لأي غرض فاحش أو غير أخلاقي&nbsp; (ك) للتدخل في الميزات الأمنية للخدمة أو أي موقع على الشبكة ذي صلة أو مواقع أخرى أو الإنترنت. نحن نحتفظ بالحق في إنهاء استخدامك للخدمة أو أي موقع ذي صلة بسبب قيامك بانتهاك أي من الاستخدامات المشروعة.</p>

<p class="text-align-right">&nbsp;</p>

<p dir="rtl"><strong>إخلاء المسؤولية:</strong></p>

<p dir="rtl">كما توافق ضمناً أن استخدامك للخدمة أو عدم قدرتك على استخدامها هو على مسؤوليتك وحدك، وأن الخدمة وجميع المنتجات والخدمات المقدمة لك من خلال هذه الخدمة هي (باستثناء ما هو منصوص عليه صراحة من قبلنا) مقدمة "كما هي" و "كما هو متاح" للاستخدام الخاص بك، دون أي تمثيل أو ضمانات أو شروط من أي نوع، سواء صريحة أو ضمنية أو مشروطة بقابلية التسويق، أو بجودة القابلية التسويقية لغرض معين، أو المتانة، أو العنوان، أو عدم الانتهاك.&nbsp;</p>

<p dir="rtl">قابلية الفصل</p>

<p dir="rtl">في حال أعتبر أي بند من بنود هذه الخدمة غير قانوني أو باطل أو غير قابل للتنفيذ، فإن هذا الحكم سيكون قابل للتنفيذ إلى أقصى حد يسمح به القانون المعمول به، ويعتبر الجزء الغير قابل للتنفيذ مفصولا عن هذه الشروط دون أن يؤثر هذا على صحة وإنفاذ أي أحكام أخرى متبقية.</p>

<p dir="rtl">الإنهاء</p>

<p dir="rtl">تبقى التزامات ومسؤوليات الأطراف التي تم التعاقد معهم قبل تاريخ الإنهاء حتى إنهاء هذه الاتفاقية لجميع الأغراض.</p>

<p dir="rtl">تبقى شروط الخدمة هذه سارية المفعول ما لم يتم انهائها من قبلك أو من قبلنا. يمكنك إنهاء بنود الخدمة هذه في أي وقت بإخطارنا بأنك لم تعد ترغب في استخدام خدماتنا أو عند التوقف عن استخدام موقعنا.</p>

<p dir="rtl">إذا أخفقت، أو شككنا في أنك فشلت في الامتثال لأي بند أو نص من بنود الخدمة هذه، يجوز لنا أيضا إنهاء هذه الاتفاقية في أي وقت دون إشعار مسبق وستظل مسؤولا عن جميع المبالغ المستحقة شامل تاريخ الإنهاء، و/ أو بناءًعلى ذلك قد يتم منعك من الوصول إلى خدماتنا (أو أي جزء منها).</p>

<p dir="rtl">&nbsp;</p>

<p dir="rtl">الشروط العامة</p>

<p dir="rtl">نحن نحتفظ بحقنا في رفض تقديم الخدمة لأي شخص لأي سبب في أي وقت.&nbsp;</p>

<p dir="rtl">أنت على دراية أن المحتوى الخاص بك (باستثناء المعلومات الخاصة ببطاقة الائتمان)، قد يتم نقلها بطريقة غير مشفرة وتشمل (أ) الإرسال عبر الشبكات المختلفة و(ب) التغييرات من أجل المطابقة والتكيف مع المتطلبات التقنية لشبكات أو أجهزة الربط. أما المعلومات المتعلقة ببطاقة الائتمان فسيتم تشفيرها دائما أثناء النقل عبر الشبكات.</p>

<p dir="rtl">أنت توافق على عدم إعادة إنتاج أو تكرار أو نسخ أو بيع أو إعادة بيع أو استغلال أي جزء من الخدمة أو استخدام الخدمة أو الوصول إلى الخدمة أو أي جهة اتصال على الموقع الإلكتروني يتم من خلالها تقديم الخدمة دون الحصول على إذن خطي صريح من قبلنا.</p>

<p dir="rtl">العناوين المستخدمة في هذه الاتفاقية تم تصميمها لراحتكم فقط ولن تحد أو تؤثر على هذه الشروط.</p>

<p dir="rtl">&nbsp;</p>

<p dir="rtl">دقة معلومات الحساب</p>

<p dir="rtl">أنت توافق على تقديم معلومات متعلقة بالاشتراك&nbsp; والحساب محدثة وكاملة ودقيقة لجميع عمليات الاشتراك التي تتم من موقعنا&nbsp; كما توافق على تحديث حسابك وأي معلومات أخرى بسرعة، بما في ذلك عنوان بريدك الإلكتروني وأرقام بطاقات الائتمان وتواريخ انتهاء الصلاحية، حتى نتمكن من إكمال معاملاتك والاتصال بك حسب الحاجة.</p>

<p dir="rtl">&nbsp;</p>

<p dir="rtl">&nbsp;أدوات اختيارية</p>

<p dir="rtl">قد نوفر لك إمكانية الوصول إلى خدمات تابعة لجهات خارجية التي لا نقوم بمراقبتها .</p>

<p dir="rtl"><strong>أنت تقر وتوافق </strong>على أننا نقدم لك امكانية الوصول إلى مثل هذه الأدوات "كما هي" و "كما هو متاح" دون أي ضمانات أو تمثيلات أو شروط من أي نوع ودون أي تأييد. لن نتحمل أي مسؤولية متعلقة باستخدام أدوات اختيارية تابعة لجهات خارجية أو متعلقة بها.</p>

<p dir="rtl">أي استخدام من قبلك لتلك الأدوات الاختيارية التي تقدم من خلال الموقع هو على مسؤوليتك الشخصية وعليك أن تتأكد من أنك على دراية بالشروط الخاصة بهذه الأدوات من قبل الجهة/الجهات الخارجية، وأنك موافق عليها.</p>

<p dir="rtl">وقد نقوم أيضا في المستقبل بتقديم خدمات و / أو ميزات جديدة من خلال الموقع الإلكتروني (بما في ذلك إصدار أدوات وموارد جديدة). كما تخضع هذه الميزات و / أو الخدمات الجديدة لبنود الخدمة هذه.</p>

<p dir="rtl">&nbsp;</p>

<p dir="rtl">روابط الجهات الخارجية&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>

<p dir="rtl">قد تتضمن موقعنا بعض المحتويات والمنتجات والخدمات المتاحة عبر خدمتنا من أطراف ثالثة.</p>

<p dir="rtl">وقد يتم توجيهك الى روابط لجهات خارجية من هذا الموقع إلى مواقع على الشبكة تابعة لجهات خارجية غير تابعة لنا. نحن لسنا مسؤولين عن فحص أو تقييم المحتوى أو الدقة، ونحن لا نضمن ولن نتحمل مسؤولية مواد صادرة عن أي طرف ثالث أو أي مواقع تابعة لها، أو عن أي مواد أو منتجات أو خدمات أخرى من أطراف ثالثة.</p>

<p dir="rtl">نحن لسنا مسؤولين عن أي ضرر أو أضرار تتعلق بالارتباط أو الاشتراك بخدمات أو موارد أو محتوى أو أي معاملات أخرى تتم من خلال موقع تابع لأي طرف ثالث. يرجى مراجعة سياسات وممارسات الجهات الخارجية بعناية والتأكد من فهمها قبل الخوض في أي معاملة معهم. كما يجب توجيه الشكاوى أو المطالبات أو المخاوف أو الأسئلة المتعلقة بمنتجات الجهات الخارجية إلى الجهة الخارجية نفسها.</p>

<p dir="rtl">&nbsp;</p>

<p dir="rtl">الأخطاء، عدم الدقة والإهمال</p>

<p dir="rtl">في بعض الأحيان قد تكون هناك معلومات على موقعنا أو ضمن الخدمة تحتوي على أخطاء مطبعية أو قد تكون غير دقيقة أو منقوصة لا سيما تلك المتعلقة بأوصاف المنتج والتكلفة والعروض الترويجية والعروض. نحن نحتفظ بالحق في تصحيح أي أخطاء أو عدم دقة أو إغفال.</p>

<p dir="rtl">ولتغيير أو تحديث المعلومات أو إلغاء الأوامر إذا كانت أي معلومات ضمن الخدمة أو على أي موقع ذي صلة غير دقيقة في أي وقت دون إشعار مسبق (بما في ذلك بعد تقديم طلبك).</p>

<p dir="rtl">نحن لا نتعهد بالقيام بأي تحديث أو تعديل أو توضيح للمعلومات المتعلقة بالخدمة أو على أي موقع على شبكة الإنترنت ذات الصلة، بما في ذلك على سبيل المثال لا الحصر، باستثناء ما يقتضيه القانون.</p>

<p class="text-align-right"><br>
&nbsp;</p>

<p dir="rtl">حقوق الملكية الفكرية.</p>

<p dir="rtl">جميع النصوص والرسومات والصور الفوتوغرافية والعلامات التجارية والشعارات والأيقونات وواجهات المستخدم والأصوات والموسيقى ومقاطع الفيديو والأعمال الفنية والبرامج ورمز الكمبيوتر (يشار إليها جميعًا بـ "المحتوى") ، بما في ذلك على سبيل المثال لا الحصر "الشكل والمظهر" ، إن التصميم والبنية ونظام الألوان والاختيار والجمع والترتيب للمحتوى الموجود على المواقع الإلكترونية مملوكة أو مرخصة لنا. هذا المحتوى محمي بموجب حقوق الطبع والنشر والعلامات التجارية والملابس التجارية ومختلف قوانين الملكية الفكرية والمنافسة غير العادلة.</p>

<p dir="rtl">باستثناء ما لدينا من إذن كتابي مسبق صريح أو وفقًا لما تسمح به القوانين المعمول بها ، لا يجوز لك نسخ أو توزيع أو إعادة إنتاج أو نسخ متطابق أو إطار أو عرض علني أو أداء علني أو ترجمة أو إنشاء أعمال مشتقة من مواقع الويب أو المحتوى أو إعادة نشرها أو نقلها ( كليًا أو جزئيًا) بأي طريقة أو بأي وسيلة للتوزيع أو النشر أو أي غرض تجاري.</p>

<p class="text-align-right">&nbsp;</p>

<p dir="rtl">لا يجوز لك عرض المحتوى ونسخه وتنزيله من المواقع الإلكترونية إلا لاستخدامك الشخصي وغير التجاري شريطة: (أ) عدم إزالة أي حقوق نشر أو إشعار ملكية من المحتوى ؛ (ب) لن يتم نسخ أو نشر هذا المحتوى على أي كمبيوتر متصل بالشبكة أو نشره في أي وسيط ؛ و (ج) لم يتم إجراء أي تعديلات على هذا المحتوى.</p>

<p dir="rtl">&nbsp;</p>

<p dir="rtl">التعويض</p>

<p dir="rtl">إنك توافق على تعويض منصة حبايبنا والدفاع عنها، وعن المؤسسات التابعة لها والشركاء والمديرين والوكلاء ومقدمي الخدمات والموظفين وجميع القائمين عليها، وتبرأتهم من أي مطالبة أو طلب، بما في ذلك أتعاب المحاماة المعقولة، التي يطالب بها أي طرف ثالث الناشئة عن خرقك لشروط الخدمة هذه أو المستندات التي تتضمنها كمرجعية، أو انتهاك لأي قانون أو حق من حقوق أي طرف ثالث.</p>

<p dir="rtl">&nbsp;&nbsp;</p>

<p dir="rtl">قابلية الفصل</p>

